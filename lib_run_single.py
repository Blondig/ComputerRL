def run_single_example_autoglm(agent, env, example, max_steps, instruction, args, example_result_dir, scores, ledger=None):
    runtime_logger = setup_logger(example, example_result_dir)
    try:
        agent.reset(runtime_logger)
    except Exception as e:
        agent.reset()

    env.reset(task_config=example)
    
    time.sleep(60) # Wait for the environment to be ready
    obs = env._get_obs() # Get the initial observation
    done = False
    step_idx = 0
    env.controller.start_recording()
    while not done and step_idx < max_steps:
        response, actions = agent.predict(
            instruction,
            obs
        )
        for action in actions:
            # Capture the timestamp before executing the action
            action_timestamp = datetime.datetime.now().strftime("%Y%m%d@%H%M%S")
            logger.info("Step %d: %s", step_idx + 1, action)
            pre_obs = obs
            obs, reward, done, info = env.step(action, args.sleep_after_execution)

            logger.info("Reward: %.2f", reward)
            logger.info("Done: %s", done)

            if ledger is not None:
                task_id = example.get("id", example_result_dir)
                if hasattr(ledger, "record_transition"):
                    ledger.record_transition(
                        task_id=task_id,
                        instruction=instruction,
                        response=response,
                        action=action,
                        pre_obs=pre_obs,
                        post_obs=obs,
                        reward=reward,
                        done=done,
                        info=info,
                        step_idx=step_idx + 1,
                    )
                else:
                    ledger.record_step(task_id, obs.get("cur_app"), action, obs.get("exe_result", ""), step_idx + 1)

            # Save screenshot and trajectory information
            with open(os.path.join(example_result_dir, f"step_{step_idx + 1}_{action_timestamp}.png"),
                      "wb") as _f:
                _f.write(obs['screenshot'])
            with open(os.path.join(example_result_dir, "traj.jsonl"), "a") as f:
                f.write(json.dumps({
                    "step_num": step_idx + 1,
                    "action_timestamp": action_timestamp,
                    "action": action,
                    "response": response,
                    "exe_result": obs.get("exe_result", ""),
                    "reward": reward,
                    "done": done,
                    "info": info,
                    "screenshot_file": f"step_{step_idx + 1}_{action_timestamp}.png"
                }))
                f.write("\n")

            if done:
                logger.info("The episode is done.")
                break

        # Invalid Action
        if not actions:
            obs = env._get_obs() # update observation

        step_idx += 1

    if not done: # not completed the task yet
        env.action_history.append('FAIL')

    result = env.evaluate()
    logger.info("Result: %.2f", result)
    scores.append(result)

    if ledger is not None:
        ledger.finalize_task(example.get("id", example_result_dir), success=result > 0)

    with open(os.path.join(example_result_dir, "result.txt"), "w", encoding="utf-8") as f:
        f.write(f"{result}\n")
    env.controller.end_recording(os.path.join(example_result_dir, "recording.mp4"))
